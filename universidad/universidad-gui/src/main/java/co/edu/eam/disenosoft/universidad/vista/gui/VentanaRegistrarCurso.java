	private void btnBuscarEstuainteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnBuscarEstuainteActionPerformed

		try {
			String cedula = tfcedulaestudiante.getText();
			Estudiante est = controlador.buscarEstudiante(cedula);

			if (est != null) {

				tfnombreestudiante.setText(est.getNombre());
				int creditos = controlador.creditosRegistrados(est);
				tfcreditosregistradosestudiante.setText(String.valueOf(creditos));

			} else {
				JOptionPane.showMessageDialog(null,
						"el estudiante con la cedula: " + cedula + " no se encuentra registrado");
			}

		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}

	}

	private void btnBuscarAsignaturaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnBuscarAsignaturaActionPerformed

		try {
			String cod = tfcodigoasignatura.getText();
			Asignatura as = controlador.buscarAsignatura(cod);

			if (as != null) {
				llenarTablaCursoAsig(as);
				tfnombreasignatura.setText(as.getNombre());
				tfcreditosasignatura.setText(String.valueOf(as.getNumeroCreditos()));
				tfTipoAsignatura.setText(String.valueOf(as.getTipo()));
			} else {
				JOptionPane.showMessageDialog(null,
						"la asignatura con el codigo " + cod + " no se encuentra registrada");
			}

		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}

	}

	/**
	 * llena la tabla Curso Asignatura
	 * 
	 * @param a
	 *            asignatura a la cual se le van a buscar los cursos
	 * @throws Exception
	 *             en caso de que ocurra una excepcion tecnica
	 */
	public void llenarTablaCursoAsig(Asignatura a) throws Exception {

		DefaultTableModel dtm = (DefaultTableModel) tablaHorario.getModel();
		dtm.setRowCount(0);

		List<Curso> cursos = controlador.listarTablaCurso(a);
		for (Curso curso : cursos) {
			String sesiones = "";
			for (int i = 0; i < curso.getSesiones().size(); i++) {	
				sesiones += curso.getSesiones().get(i).getDia()+ " De " + 
				curso.getSesiones().get(i).getHoraInicio() + " A " + curso.getSesiones().get(i).getHoraFinal()+ " " ;
				}
				dtm.addRow(new Object[]{
						curso.getCodigo(),sesiones
				} );
			}
		}

	

