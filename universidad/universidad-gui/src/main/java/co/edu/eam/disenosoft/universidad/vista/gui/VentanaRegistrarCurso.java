	private void btnRegistrarCursoActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnRegistrarCursoActionPerformed

		try {
			int pos = tablaHorario.getSelectedRow();
			String cedula = tfcedulaestudiante.getText();
			DefaultTableModel dtm = (DefaultTableModel) tablaHorario.getModel();
			if (pos >= 0) {
				String cod = (String) dtm.getValueAt(pos, 0);
				Curso cur = controlador.buscarCurso(cod);
				Estudiante est = controlador.buscarEstudiante(cedula);
				if ((cur != null) && (est != null)) {
					RegistroCurso regCur = new RegistroCurso(est, cur);
					controlador.crearCruso(regCur);
					
					JOptionPane.showMessageDialog(null, "se ha registrado en el curso");
				} else {
					JOptionPane.showMessageDialog(null, "verifique que el estudiante este registrado y que el curso exista");

				}
			}else{
				JOptionPane.showMessageDialog(null, "seleccione una fila");

			}

		} catch (ExcepcionNegocio e) {
			JOptionPane.showMessageDialog(null, e.getMessage());
		} catch (Exception exc) {
			exc.printStackTrace();
		}

	}

